function randInterval(min, max) {
    return Math.floor(Math.random() * (max - min)) + min;
}

const randCity = () => {
    const numCity = randInterval(0, citys.length);
    return [citys[numCity], numCity];
}

const randPoint = (numCity) => {
    return points[numCity][randInterval(0, points[numCity].length)];
}

const revertPoints = (coordinates) => {
    let points = coordinates.split(', ');
    let x = Number(points[0]);
    let y = Number(points[1]);
    if (x < 0) {
        x = x * -1;
    }
    return y + ',' + x;
}

